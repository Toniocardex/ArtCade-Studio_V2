#!/usr/bin/env python3
# =============================================================================
# emit-asset-key-header.py — write the C++ asset-key header for the build
#
# Called by CMake at configure time. Resolves the key via artcade_keytool
# (env > secret file > dev key) and writes a header the C++ decrypt unit
# includes. Only rewrites the file when its content changes, so configure
# re-runs don't force needless recompiles.
#
# Usage:
#   python tools/emit-asset-key-header.py <output_header_path>
# =============================================================================

import sys

from artcade_keytool import resolve_key


def render_header(key: bytes, is_dev: bool) -> str:
    body = ", ".join(f"0x{b:02x}" for b in key)
    dev = "true" if is_dev else "false"
    return (
        "// Auto-generated by tools/emit-asset-key-header.py — DO NOT EDIT.\n"
        "// The asset-encryption key is resolved at CMake configure time.\n"
        "#pragma once\n"
        "#include <cstdint>\n\n"
        "namespace ArtCade {\n"
        f"inline constexpr uint8_t  kArtcadeAssetKey[32] = {{ {body} }};\n"
        f"inline constexpr bool     kArtcadeAssetKeyIsDev = {dev};\n"
        "} // namespace ArtCade\n"
    )


def main() -> int:
    if len(sys.argv) != 2:
        print("usage: emit-asset-key-header.py <output_header_path>", file=sys.stderr)
        return 2

    out_path = sys.argv[1]
    key, is_dev = resolve_key(warn=True)
    content = render_header(key, is_dev)

    try:
        with open(out_path, encoding="utf-8") as f:
            if f.read() == content:
                return 0  # unchanged — avoid touching mtime
    except OSError:
        pass

    with open(out_path, "w", encoding="utf-8") as f:
        f.write(content)
    return 0


if __name__ == "__main__":
    sys.exit(main())
