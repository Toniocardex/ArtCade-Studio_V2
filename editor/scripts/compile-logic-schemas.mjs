/**
 * Build-time Ajv compilation - no new Function() at runtime (Tauri CSP-safe).
 * Run: npm run compile-schemas
 * Output: src/utils/logic-board/validators.generated.ts
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import Ajv from 'ajv'
import addFormats from 'ajv-formats'
import standaloneCode from 'ajv/dist/standalone/index.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const editorRoot = path.resolve(__dirname, '..')
const schemaDir = path.join(editorRoot, 'src/schemas/logic-board')
const outFile = path.join(editorRoot, 'src/utils/logic-board/validators.generated.ts')

const SCHEMA_BASE = 'https://artcade.local/logic/'
const TARGET_SCHEMA_ID = `${SCHEMA_BASE}target-selector.schema.json`
const VALUE_SCHEMA_ID = `${SCHEMA_BASE}value-source.schema.json`

function readJson(name) {
  return JSON.parse(fs.readFileSync(path.join(schemaDir, name), 'utf8'))
}

function deepClone(value) {
  return JSON.parse(JSON.stringify(value))
}

/** Keep local $defs intact while pointing repeated external refs at shared schemas. */
function prepareRefs(schema) {
  const walk = (node) => {
    if (!node || typeof node !== 'object') return node
    if (Array.isArray(node)) return node.map(walk)
    const out = {}
    for (const [key, value] of Object.entries(node)) {
      if (key === '$ref' && value === './target-selector.schema.json') {
        out[key] = TARGET_SCHEMA_ID
      } else if (key === '$ref' && value === './value-source.schema.json') {
        out[key] = VALUE_SCHEMA_ID
      } else {
        out[key] = walk(value)
      }
    }
    return out
  }
  return walk(deepClone(schema))
}

function enrichBoardSchema(board, index, triggers, actions, conditions) {
  const schema = prepareRefs(board)
  const eventItem = schema.properties?.events?.items
  if (!eventItem?.properties) return schema

  const actionTypes = [...index.actions, ...(index.legacyActions ?? [])]
  const conditionTypes = [...index.conditions, ...(index.legacyConditions ?? [])]
  const triggerUnion = {
    oneOf: index.triggers.map((type) => prepareRefs(triggers[type])),
  }
  const actionUnion = {
    oneOf: actionTypes.map((type) => prepareRefs(actions[type])),
  }
  const conditionUnion = {
    oneOf: conditionTypes.map((type) => prepareRefs(conditions[type])),
  }

  eventItem.properties.trigger = triggerUnion
  eventItem.properties.actions = { type: 'array', items: actionUnion }
  eventItem.properties.conditions = { type: 'array', items: conditionUnion }
  eventItem.properties.conditionRoot = conditionUnion
  eventItem.properties.elseActions = { type: 'array', items: actionUnion }
  return schema
}

const index = readJson('index.json')
const boardSchema = readJson('board.schema.json')
const triggers = readJson('triggers.json')
const actions = readJson('actions.json')
const conditions = readJson('conditions.json')
const targetSelector = readJson('target-selector.schema.json')
const valueSource = readJson('value-source.schema.json')

const triggerTypes = index.triggers
const actionTypes = [...index.actions, ...(index.legacyActions ?? [])]
const conditionTypes = [...index.conditions, ...(index.legacyConditions ?? [])]

const schemas = {
  validateBoard: {
    ...enrichBoardSchema(boardSchema, index, triggers, actions, conditions),
    $id: `${SCHEMA_BASE}board`,
  },
  validateTriggerUnion: {
    $id: `${SCHEMA_BASE}trigger-union`,
    oneOf: triggerTypes.map((type) => prepareRefs(triggers[type])),
  },
  validateActionUnion: {
    $id: `${SCHEMA_BASE}action-union`,
    oneOf: actionTypes.map((type) => prepareRefs(actions[type])),
  },
  validateConditionUnion: {
    $id: `${SCHEMA_BASE}condition-union`,
    oneOf: conditionTypes.map((type) => prepareRefs(conditions[type])),
  },
}

const ajv = new Ajv({ allErrors: true, strict: false, code: { source: true, esm: true } })
addFormats(ajv)
ajv.addSchema({ ...prepareRefs(targetSelector), $id: TARGET_SCHEMA_ID })
ajv.addSchema({ ...prepareRefs(valueSource), $id: VALUE_SCHEMA_ID })
for (const schema of Object.values(schemas)) ajv.addSchema(schema)

let generated = standaloneCode(ajv, Object.fromEntries(
  Object.entries(schemas).map(([name, schema]) => [name, schema.$id]),
))
generated = generated.replace(/"use strict";\s*/g, '')
generated = generated.replace(
  /const (func\d+) = require\("ajv\/dist\/runtime\/ucs2length"\)\.default;/g,
  'const $1 = ucs2length;',
)

const mapEntries = []
for (const [index, type] of triggerTypes.entries())
  mapEntries.push(`  'trigger:${type}': scopedValidator(validateTriggerUnion, ${index}),`)
for (const [index, type] of actionTypes.entries())
  mapEntries.push(`  'action:${type}': scopedValidator(validateActionUnion, ${index}),`)
for (const [index, type] of conditionTypes.entries())
  mapEntries.push(`  'condition:${type}': scopedValidator(validateConditionUnion, ${index}),`)

const header = `// AUTO-GENERATED by scripts/compile-logic-schemas.mjs - do not edit.
// Regenerate: npm run compile-schemas
// @ts-nocheck

import ucs2length from 'ajv/dist/runtime/ucs2length.js'

`
const footer = `
function scopedValidator(validate, branchIndex) {
  const branchMarker = \`/oneOf/\${branchIndex}/\`
  const scoped = (data) => {
    const valid = validate(data)
    if (valid) {
      scoped.errors = null
      return true
    }
    const branchErrors = (validate.errors ?? []).filter((error) =>
      error.schemaPath.includes(branchMarker),
    )
    scoped.errors = branchErrors.length > 0 ? branchErrors : validate.errors
    return false
  }
  scoped.errors = null
  return scoped
}

export const componentValidators = {
${mapEntries.join('\n')}
}
`

fs.writeFileSync(outFile, header + generated.trim() + footer, 'utf8')
console.log(
  `[compile-schemas] Wrote 4 shared validators -> ${path.relative(editorRoot, outFile)}`,
)
