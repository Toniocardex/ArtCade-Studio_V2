/**
 * Regenerates boot chrome assets from boot-surfaces.json.
 * Run after changing surface tokens: npm run sync-boot-chrome
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const surfaces = JSON.parse(readFileSync(join(root, 'boot-surfaces.json'), 'utf8'))

const bootJs = `// AUTO-GENERATED by scripts/sync-boot-chrome.mjs — do not edit.
// Source of truth: editor/boot-surfaces.json
(function () {
  var SURFACES = ${JSON.stringify(surfaces, null, 2)}
  var theme = 'dark'
  try {
    var stored = localStorage.getItem('artcade-theme')
    if (stored === 'dark' || stored === 'light') theme = stored
    else if (window.matchMedia && window.matchMedia('(prefers-color-scheme: light)').matches) theme = 'light'
  } catch (_e) { /* private mode */ }
  var c = SURFACES[theme]
  document.documentElement.dataset.theme = theme
  document.documentElement.style.backgroundColor = c.bg
  document.documentElement.style.color = c.text
  var style = document.createElement('style')
  style.id = 'boot-chrome'
  style.textContent =
    'html,body,#root{width:100%;height:100%;margin:0;padding:0;overflow:hidden;background:' + c.bg + ';color:' + c.text + '}' +
    '#boot-shell{position:fixed;inset:0;z-index:2147483646;display:flex;align-items:center;justify-content:center;background:' + c.bg + ';color:' + c.text + ';font-family:system-ui,-apple-system,Segoe UI,sans-serif;font-size:1.25rem;font-weight:700;letter-spacing:-0.02em;user-select:none}'
  document.head.appendChild(style)
})()
`

const { dark, light } = surfaces

const criticalCss = `/* Critical layout + theme fallback if bundled CSS is blocked (Tauri CSP).
 * --bg / --text: AUTO-GENERATED from boot-surfaces.json (npm run sync-boot-chrome).
 * Other tokens mirror index.css fallbacks for offline shell paint. */
html, body, #root {
  width: 100%;
  height: 100%;
  min-height: 100vh;
  margin: 0;
  padding: 0;
  overflow: hidden;
}
:root,
[data-theme="dark"] {
  --bg: ${dark.bg};
  --panel: #313131;
  --border: #484848;
  --text: ${dark.text};
  --muted: #A2A2A2;
  --accent: #7A9C7E;
}
[data-theme="light"] {
  --bg: ${light.bg};
  --panel: #F5F3EE;
  --border: #C4C0B8;
  --text: ${light.text};
  --muted: #5A5A5A;
  --accent: #5D7A61;
}
body {
  background: var(--bg);
  color: var(--text);
  font-family: ui-sans-serif, system-ui, -apple-system, 'Segoe UI', sans-serif;
}
`

writeFileSync(join(root, 'public', 'boot-theme-init.js'), bootJs, 'utf8')
writeFileSync(join(root, 'public', 'critical-layout.css'), criticalCss, 'utf8')

const indexPath = join(root, 'index.html')
let indexHtml = readFileSync(indexPath, 'utf8')
const wasmPreload = '<link rel="preload" href="./runtime/game.wasm" as="fetch" crossorigin />'
if (!indexHtml.includes('runtime/game.wasm')) {
  indexHtml = indexHtml.replace(
    '<link rel="stylesheet" href="./critical-layout.css" />',
    `${wasmPreload}\n    <link rel="stylesheet" href="./critical-layout.css" />`,
  )
}
writeFileSync(indexPath, indexHtml, 'utf8')

console.log('[sync-boot-chrome] Wrote public/boot-theme-init.js')
console.log('[sync-boot-chrome] Wrote public/critical-layout.css (--bg/--text from boot-surfaces.json)')
console.log('[sync-boot-chrome] Ensured index.html WASM preload link')
