/**
 * Regenerates public/boot-theme-init.js from boot-surfaces.json.
 * Run after changing surface tokens: npm run sync-boot-chrome
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const surfaces = JSON.parse(readFileSync(join(root, 'boot-surfaces.json'), 'utf8'))

const out = `// AUTO-GENERATED by scripts/sync-boot-chrome.mjs — do not edit.
// Source of truth: editor/boot-surfaces.json
(function () {
  var SURFACES = ${JSON.stringify(surfaces, null, 2)}
  var theme = 'dark'
  try {
    var stored = localStorage.getItem('artcade-theme')
    if (stored === 'dark' || stored === 'light') theme = stored
    else if (window.matchMedia && window.matchMedia('(prefers-color-scheme: light)').matches) theme = 'light'
  } catch (_e) { /* private mode */ }
  var c = SURFACES[theme]
  document.documentElement.dataset.theme = theme
  document.documentElement.style.backgroundColor = c.bg
  document.documentElement.style.color = c.text
  var style = document.createElement('style')
  style.id = 'boot-chrome'
  style.textContent =
    'html,body,#root{width:100%;height:100%;margin:0;padding:0;overflow:hidden;background:' + c.bg + ';color:' + c.text + '}' +
    '#boot-shell{position:fixed;inset:0;z-index:2147483646;display:flex;align-items:center;justify-content:center;background:' + c.bg + ';color:' + c.text + ';font-family:system-ui,-apple-system,Segoe UI,sans-serif;font-size:1.25rem;font-weight:700;letter-spacing:-0.02em;user-select:none}'
  document.head.appendChild(style)
})()
`

writeFileSync(join(root, 'public', 'boot-theme-init.js'), out, 'utf8')
console.log('[sync-boot-chrome] Wrote public/boot-theme-init.js')
